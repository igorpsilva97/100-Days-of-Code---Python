# Addition
print(123 + 456)

# Subtraction
print(7 - 3)

# Multiplication
print(8 * 9)

# Division
print(100 / 5)

# Floor Division (Integer Division)
print(10 // 3)

# Modulus (Remainder)
print(10 % 3)

# Exponentiation (Power)
print(2 ** 4)

# Addition before Multiplication (using parentheses)
# Without parens, 5 * 10 would run first.
print((2 + 3) * 10)

# Subtraction before Division (using parentheses)
# Without parens, 20 / 2 would run first.
print((50 - 20) / 2)

# Addition before Exponentiation (using parentheses)
# Without parens, 3 ** 2 would run first.
print((4 + 3) ** 2)

# Grouping complex operations (using parentheses)
# Solves (100 / 10) and (2 * 3) separately before subtracting.
print((100 / 10) - (2 * 3))

# Addition before Modulus (using parentheses)
# Without parens, 5 % 2 would run first.
print((10 + 5) % 2)